package Day23_CustomMethods_Void;

public class evenNumbers {
    public static void evenNumber() {
        for (int i = 0; i < 100; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " ");
            }
        }
        System.out.println();
    }
}
